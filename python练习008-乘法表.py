# 题目：输出 9*9 乘法口诀表。
# 程序分析：分行与列考虑，共9行9列，i控制行，j控制列。

print (' --------------------------------九九乘法表\
-------------------------------------')
for i in range(1,10):
    if i == 1:
        pass
    else:
        print()
    for j in range(1,i+1):
        print ("%1d×%1d=%2d" %(i,j,i*j),end="   ")
print ('\n','--------------------------------------\
---------------------------------------')
